public class Test6 {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        boolean b1 = a > b; // оператор сравнения >
        boolean b2 = a < b; // оператор сравнения <
        System.out.println(b1);
        System.out.println(b2);
        // оператор сравнения =>
        // оператор сравнения <=
        // оператор сравнения ==
        boolean b3 = a == b;
        System.out.println(b3);
        // оператор != - НЕРОВНО
        boolean b4 = a != b;
        System.out.println(b4);

    }
}
